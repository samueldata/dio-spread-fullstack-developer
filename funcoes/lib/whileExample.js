// Exemplo 1 - While
function exemploWhile() {
    let num = 0

    while (num <= 5) {
        console.log(num);
        num++
    }
}

// exemploWhile()
// 0
// 1
// 2
// 3
// 4
// 5

// Exemplo 2 - Do... While
function exemploDoWhile() {
    let num = 0;

    do {
        console.log(num);
        num++;
    } while (num <= 5)
}
// exemploDoWhile()

// 0
// 1
// 2
// 3
// 4
// 5

